//Write a C program that includes variables, constants, and comments. Declare and use different data types 
//(int, char, float) and display their values

#include <stdio.h>
#define PI 3.14
 main() 
 {

   int age = 20;        
    float height = 5.5;   
    char grade = 'A';    
    const int year = 2025;  
    printf("Age: %d\n", age);
    printf("Height: %f\n", height);   
    printf("Grade: %c\n", grade);
    printf("Year: %d\n", year);
    printf("PI: %f\n", PI); 


}


